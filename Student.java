public class Student
{
public int id;
public String name, aadharNumber,mobileNumber;
public String isIndian;
//ID
public int getId()
{
return this.id;
}
public void setId(int SId)
{
this.id=SId;
}
//NAME
public String getName()
{
return this.name;
}
public void setName(String SName)
{
this.name=SName;
}
//AADHAR NUMBER
public String getAadharNumber()
{
return this.aadharNumber;
}
public void setAadharNumber(String SAadharNumber)
{
this.aadharNumber=SAadharNumber;
}
//MOBILE NUMBER
public String getMobileNumber()
{
return this.mobileNumber;
}
public void setMobileNumber(String SMobileNumber)
{
this.mobileNumber=SMobileNumber;
}
//IS INDIAN
public String getIsIndian()
{
return this.isIndian;
}
public void setIsIndian(String SIsIndian)
{
this.isIndian=SIsIndian;
}

}